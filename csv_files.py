import csv

# Данные для файла с задачами
tasks_data = [
    ["Задача", "Статус", "Команда", "Оценка"],
    ["Задача 1", "Завершено", "Команда 1", 8],
    ["Задача 2", "В процессе", "Команда 2", 7],
    ["Задача 3", "Не начато", "Команда 3", 5],
    ["Задача 4", "Завершено", "Команда 1", 9],
    ["Задача 5", "В процессе", "Команда 2", 6],
]

# Данные для файла с оценками команд
sprint_data = [
    ["Спринт", "Команда", "Оценка"],
    ["Спринт 1", "Команда 1", 8],
    ["Спринт 1", "Команда 2", 7],
    ["Спринт 1", "Команда 3", 9],
    ["Спринт 2", "Команда 1", 7],
    ["Спринт 2", "Команда 2", 6],
]

# Данные для дополнительного файла с метками
additional_data = [
    ["Команда", "Количество задач", "Прогресс", "Оценка"],
    ["Команда 1", 10, "80%", 8],
    ["Команда 2", 12, "65%", 7],
    ["Команда 3", 15, "90%", 9],
]


# Функция для записи данных в CSV файл
def write_to_csv(filename, data):
    with open(filename, mode="w", newline="", encoding='utf-8') as file:
        writer = csv.writer(file)
        writer.writerows(data)


# Запись данных в соответствующие файлы
write_to_csv("tasks_data.csv", tasks_data)
write_to_csv("sprint_data.csv", sprint_data)
write_to_csv("additional_data.csv", additional_data)

print("CSV файлы успешно созданы.")
